package ilainaRallye ;

public class Dateheure{
    int heure ;
    int minute ;
    int seconde ;

    public int getHeure(){
        return heure ;
    }

    public int getMinute(){
        return minute ;
    }

    public int getSeconde(){
        return seconde ;
    }

    public void setHeure(int nov){
        heure=nov ;
    }

    public void setMinute(int nov){
        minute=nov ;
    }

    public void setSeconde(int nov){
        seconde=nov ;
    }
}